// Measures the registration cost of bind() itself (indexKey + saveMapping per secondary type).
// Uses the parallel BindPerfs hierarchy (see bind_classes.kt) so the baseline Perfs classes
// stay untouched and comparable to historical bench_results.txt rows.
//
// Spread across 4 interfaces (IA/IB/IC/ID) — within each group, last-writer-wins on the
// registry mapping. Fine here: we're measuring bind() throughput, not final resolution.
//
// Split into 4 helper functions because 400 `singleOf + bind` calls in one lambda overflow
// the JVM 64KB method size limit — the structural ceiling Zac flagged in the Metro analysis.
fun perfModule400WithBinds() = module {
    bindGroup1()
    bindGroup2()
    bindGroup3()
    bindGroup4()
}

